# Program menghitung luas segitiga
# Rumus: 1/2 * alas * tinggi

# Meminta input alas dan tinggi dari pengguna
alas = float(input("Masukkan nilai alas segitiga: "))
tinggi = float(input("Masukkan nilai tinggi segitiga: "))

# Menghitung luas segitiga
luas = 0.5 * alas * tinggi

# Menampilkan hasil
print(f"Luas segitiga dengan alas {alas} dan tinggi {tinggi} adalah: {luas}")